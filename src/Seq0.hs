
import Prelude hiding ( null, head, tail, last, init, (++) )

--------------------------------------------------------------------------------

type Seq a

--------------------------------------------------------------------------------

-- main operations

null :: Seq a -> Bool

head :: Seq a -> a
cons :: a -> Seq a -> Seq a
tail :: Seq a -> Seq a

(++) :: Seq a -> Seq a -> Seq a

--------------------------------------------------------------------------------

-- symmetrical

last :: Seq a -> a
snoc :: Seq a -> a -> Seq a
init :: Seq a -> Seq a

--------------------------------------------------------------------------------

-- not dealt with here

(!!) :: Seq a -> Int -> a

--------------------------------------------------------------------------------

